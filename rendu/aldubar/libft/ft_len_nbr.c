#include "libft.h"

size_t	ft_len_nbr(int nb)
{
	size_t			i;
	unsigned int	n;

	i = 1;
	n = nb;
	if (nb < 0)
	{
		n = -nb;
		i++;
	}
	while (n >= 10)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
