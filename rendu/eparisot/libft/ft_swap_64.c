#include "libft.h"

uint64_t	ft_swap_64(uint64_t n)
{
	n = ((n << 8) & 0xFF00FF00FF00FF00) | ((n >> 8) & 0x00FF00FF00FF00FF);
	n = ((n << 16) & 0xFFFF0000FFFF0000) | ((n >> 16) & 0x0000FFFF0000FFFF);
	return ((n << 32) | (n >> 32));
}
