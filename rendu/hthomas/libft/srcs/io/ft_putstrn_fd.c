#include "../../includes/libft.h"

void	ft_putstrn_fd(const char *str, int fd, unsigned int n)
{
	size_t	i;

	i = 0;
	if (!str)
		return ;
	while (n-- && str[i])
		ft_putchar_fd(str[i++], fd);
}
