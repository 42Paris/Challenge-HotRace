#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *newitem)
{
	t_list	*tmp;

	if (alst == NULL)
		return ;
	tmp = *alst;
	if (tmp)
	{
		while (tmp->next)
			tmp = tmp->next;
		tmp->next = newitem;
	}
	else
		ft_lstadd_front(alst, newitem);
}
