#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*fstr;

	i = 0;
	if (s == 0 || f == 0)
		return (NULL);
	while (s[i])
		i++;
	fstr = (char *)malloc(i + 1);
	if (fstr == 0)
		return (NULL);
	i = 0;
	while (s[i])
	{
		fstr[i] = f(i, s[i]);
		i++;
	}
	fstr[i] = '\0';
	return (fstr);
}
